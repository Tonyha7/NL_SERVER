        public static void Main(string[] args)
        {
            if (args.Length > 0 && args[0] == "testdump") {
                using var testdb = new LiteDatabase(DbPath);
                var testuserObj = testdb.GetCollection<User>("users").FindOne(x => x.Username == "Tonyha7");
                if (testuserObj != null) {
                    var logs = testdb.GetCollection<LogEntry>("logs").Find(x => x.UserId == testuserObj.Id).OrderBy(x => x.EntryId).ToList();
                    var configLogs = logs.Where(x => x.EntryType == "Config").Select(x => new LogEntry { EntryId=x.EntryId, Timestamp=x.Timestamp, EntryType=x.EntryType, Author=x.Author, Name=x.Name }).ToList();
                    var scriptLogs = logs.Where(x => x.EntryType == "Script").Select(x => new LogEntry { EntryId=x.EntryId, Timestamp=x.Timestamp, EntryType=x.EntryType, Author=x.Author, Name=x.Name }).ToList();
                    var moduleBin = ModuleBuilder.BuildUserModule("Tonyha7", configLogs, scriptLogs);
                    Console.WriteLine("Generated bytes: " + (moduleBin?.Length ?? 0));
                }
                return;
            }
            if (!Directory.Exists(BinsPath))
                Directory.CreateDirectory(BinsPath);

            using (var db = new LiteDatabase(DbPath))
            {
                var users = db.GetCollection<User>("users");
                users.EnsureIndex(x => x.Username, true);
                users.EnsureIndex(x => x.AuthToken, true);
                var logs = db.GetCollection<LogEntry>("logs");
                logs.EnsureIndex(x => x.UserId);
            }

            var builder = WebApplication.CreateBuilder(args);
            builder.WebHost.ConfigureKestrel(options =>
            {
                var pfxPath = Path.Combine(AppDomain.CurrentDomain.BaseDirectory, "certs", "cert.pfx");
                var cert = TlsGenerator.EnsureSelfSignedCert(pfxPath);

                options.Listen(IPAddress.Any, 30030, listenOptions => 
                {
                    listenOptions.UseHttps(cert); // WebSocket port with TLS (WSS)
                }); 
                options.Listen(IPAddress.Any, 30031); // API / Admin port (HTTP)
            });
            
            var app = builder.Build();

            app.UseWebSockets(new WebSocketOptions { KeepAliveInterval = TimeSpan.FromMinutes(2) });

            // Middleware for WebSocket parsing
            app.Use(async (context, next) =>
            {
                if (context.Connection.LocalPort == 30030)
                {
                    if (context.WebSockets.IsWebSocketRequest)
                    {
                        var ws = await context.WebSockets.AcceptWebSocketAsync();
                        await HandleWebSocket(context, ws);
                    }
                    else
                    {
                        context.Response.StatusCode = 400;
                    }
                }
                else
                {
                    await next(context);
                }
            });

            MapAdminEndpoints(app);
            app.Run();
        }

